    let currentLang = localStorage.getItem('lang') || 'fa';
    let observer;

    function applyLanguage(lang) {
      currentLang = lang;
      localStorage.setItem('lang', lang);
      document.documentElement.lang = lang;
      document.documentElement.dir = lang === 'fa' ? 'rtl' : 'ltr';
      document.querySelectorAll('[data-i18n]').forEach(el => {
        const keys = el.getAttribute('data-i18n').split('.');
        let value = translations[lang];
        for (let k of keys) value = value[k];
        if (value) el.textContent = value;
      });
      if (window.typedRole) window.typedRole.destroy();
      window.typedRole = new Typed('#typed-role', {
        strings: lang === 'fa' ? ['توسعه‌دهنده فول‌استک | Unity Expert', 'متخصص هوش مصنوعی', 'کارشناس امنیت سایبری'] : ['Full Stack Developer | Unity Expert', 'AI Automation Specialist', 'Cyber Security Expert'],
        typeSpeed: 80, backSpeed: 50, backDelay: 2000, loop: true, showCursor: true, cursorChar: '|'
      });
      document.getElementById('langText').textContent = lang === 'fa' ? 'EN' : 'FA';
      document.getElementById('langTextMobile').textContent = lang === 'fa' ? 'EN' : 'FA';
    }

    function initTypedJS() {
      new Typed('#typed-name', { strings: ['Bardia Klidari'], typeSpeed: 100, showCursor: false });
    }

    function initScrollReveal() {
      observer = new IntersectionObserver((entries) => {
        entries.forEach(entry => { if (entry.isIntersecting) entry.target.classList.add('revealed'); });
      }, { threshold: 0.1, rootMargin: '0px 0px -50px 0px' });
      document.querySelectorAll('.scroll-reveal').forEach(el => observer.observe(el));
    }

    document.getElementById('langSwitch')?.addEventListener('click', () => applyLanguage(currentLang === 'fa' ? 'en' : 'fa'));
    document.getElementById('langSwitchMobile')?.addEventListener('click', () => applyLanguage(currentLang === 'fa' ? 'en' : 'fa'));
    
    function toggleTheme() {
      const isDark = document.body.classList.contains('dark');
      document.body.classList.toggle('dark');
      document.querySelectorAll('.theme-toggle-wrapper').forEach(el => el.classList.toggle('dark'));
      localStorage.setItem('darkMode', !isDark);
    }
    document.getElementById('themeToggle')?.addEventListener('click', toggleTheme);
    document.getElementById('themeToggleMobile')?.addEventListener('click', toggleTheme);

    if (localStorage.getItem('darkMode') === 'true') {
      document.body.classList.add('dark');
      document.querySelectorAll('.theme-toggle-wrapper').forEach(el => el.classList.add('dark'));
    }

    window.addEventListener('load', () => {
      applyLanguage(currentLang);
      initTypedJS();
      initScrollReveal();
      document.body.style.opacity = '0';
      document.body.style.transition = 'opacity 0.5s';
      setTimeout(() => { document.body.style.opacity = '1'; }, 100);
    });